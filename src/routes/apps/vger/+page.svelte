<h2>Infobox</h2>
<p>
    Once you have a selected card, you can see a variety of information on the sidebar: The image of the selected item, its name and type and more. You can always click on the name
    to open the wiki page of the respective item. On a Starship Trait infobox you can also click on the ship names under 'Obtained from' to see information on the ship on the STO-Wiki.
    <br><br>The now following description may be not be perfectly readable and akward formatted. This is due to the fact that all the data VGER uses is drawn from the STO-Wiki, that
    has a unique formatting style not available outside of the wiki itself. Also the descriptions might be empty completely due to missing data, the STO-Wiki is maintained by only
    a small number of volunteers after all. In both cases it is advised to visit the wiki page to see all the information.
</p>
