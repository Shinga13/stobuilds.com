<h1>Introduction</h1>
<p>
    VGER is a Web-Application developed to help players identify items from Star Trek Online. This game has a huge amount of items: 
    About 250 Starship Traits and 400 Personal Traits are a lot to keep in ones head. Furthermore there are 23 equipment categories including Kits, Weapons, 
    Shields, Consoles and Devices that in total contain about 2500 different items. That is far too much to memorize and especially newer players fail to identify 
    the items they've seen on shared builds or on their favourite Youtuber's videos. VGER is designed to solve these inconveniences.
</p>
<p>
    VGER features five modules of which four are Visual Glossaries: Starship Traits, Personal Traits, Space and Ground Equipment. The fifth module is an acronym
    finder for STO-specific terms and abbreviations. In all five modules you can use the top search bar if you want to find something specific. The four 
    Visual Glossary modules also have appropriate filters on the sidebar to narrow down the query. Furthermore you can select between three card sizes on the sidebar 
    and VGER also supports a specific mobile device layout.
</p>
<p>
    You may notice, that there is no module aimed at Bridge Officer Abilities, this is due to the fact that the 
    STO-Wiki already has an ideal page to find those abilities: 
    <a href='https://stowiki.net/wiki/Bridge_officer_and_kit_abilities' referrerpolicy='no-referrer' target='_blank' rel='noopener noreferrer'>Bridge Officer and Kit Abilities</a> 
        -- Here you can simply use the table of contents to navigate to the wanted section and look the respective ability up in the table.
</p>

