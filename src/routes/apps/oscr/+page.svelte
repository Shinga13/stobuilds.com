<h1 class='heading'>Open Source Combatlog Reader</h1>
<h1>Introduction</h1>
<p>
    OSCR is a feature-rich combatlog reader / parser for Star Trek Online. It is supposed to replace the STO Combat Meter as parser for the DPS Tables once it's development is finished. 
    Currently the app is in beta state, awaiting the new leaderboards to be set up.
</p>

<h1>Downloads</h1>
<p>
    OSCR can be downloaded from the <a href='https://github.com/STOCD/OSCR-UI/releases' target='_blank' referrerpolicy='no-referrer' rel='noopener noreferrer'>GitHub page</a>. 
    After downloading, unpack the archive and run the app by double-clicking the executable.
</p>
