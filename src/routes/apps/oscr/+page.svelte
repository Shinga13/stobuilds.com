<h1 class='heading'>Open Source Combatlog Reader</h1>
<h1>Introduction</h1>
<p>
    OSCR is a feature-rich combatlog reader / parser for Star Trek Online. Features include an overview and analysis page with a detailed and complete breakdown of all used abilities, access to the DPS Tables and a variety of Combatlog manipulation features. There is also a LiveParser overlay which allows to view combat metrics while fighting. Currently it is the recommended parser for interacting with the DPS Tables. The Tables can also be visited <a href='https://oscr.stobuilds.com/ui/' target='_blank' referrerpolicy='no-referrer' rel='noopener noreferrer'>online</a>. 
</p>

<h1>Downloads</h1>
<p>
    OSCR can be downloaded from the <a href='https://github.com/STOCD/OSCR-UI/releases' target='_blank' referrerpolicy='no-referrer' rel='noopener noreferrer'>GitHub page</a>. 
    After downloading, unpack the archive and run the app by double-clicking the executable.
</p>
